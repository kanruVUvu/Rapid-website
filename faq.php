WPFC will be forming a relationship with the local schools in 2021 to have a targeted youth program aimed at boys and girls ages 12 – 18 years of age. The program will be fully funded and will based around 2 weekly sessions held at WPFC. These sessions will be based around football but will also touch base on core values including leadership, resilience and wellbeing. Both the City of Wanneroo and The City of Joondalup have been briefed on this program and are very supportive of it. WPFC will also be engaging with the local police as a key stakeholder in this program. The belonging to a club will be the key learning for the youth involved and all participants will receive WPFC memberships as well as further opportunities.<br/><br/>

<b>After Schools Program</b>
<br/>
<br/>

The after-school program is a 8 week x school term clinic facilitated by certified coaches to encourage school children to turn to sport after school instead of hanging around the wrong crowd and participating in anti-social or criminal behaviour. Our aim is to reach out to youth who may not necessarily participate in sports. Emphasis here is on health, fitness and well-being through a fun activity where current skill levels should not hold anyone back. We believe team sports help young people learn to be accountable, responsible, disciplined, passionate and driven to improve. They expose them to meet new friends. Apart from character building, many studies have shown sports – especially team sports help students do better academically. For instance, repetitive drills of set pieces, memorizing<br/> tactics and team strategies are skillsets that can be directly relevant to classwork. Goal – setting and leadership skills learnt on the field<br/> can also be transferred to the classroom.<br/><br/>    


<b>Career Advancement Program</b>
<br>
<br>
Beginning in term 1 2021. The WPFC will be delivering a career advancement program. The first course delivered will be Certificate 111 in Career Advancement. This qualification is designed to provide course participants with the essential foundation skills required to enter<br/> and thrive in the workforce.<br/><br/>

The range of skills developed in the course include both specific skills relative to the workplace, as well as a strong focus on soft skills<br/> such as; Positive Psychology, Growth V’s Fixed Mindsets, Emotional Intelligence, Mental Health Literacy, Goal Setting, Forming Good<br/> Habits, Attitudes and Beliefs, Resilience, Team Work, Leadership, Negotiation, Persuasion and Motivation. Much of the course<br/> content/learning materials supports the work of the PAHL (Psychology of Active, Healthy Living) Group of the University of Western<br/> Australia (UWA) and it is the intention of the course providers to work closely with PAHL throughout the ongoing review and refinement of<br/> the course.<br/><br/>

This qualification is designed to provide students with a deeper, more meaningful sense of self that provides the foundation for students<br/> to apply high level learning strategies to facilitate lifelong learnings and transferable generic skills across various industries.
